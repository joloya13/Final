package web.model;

public class GenreConsole {
    private int genreId;
    private String console;

    public GenreConsole() {
    }

    public int getGenre() {
        return genreId;
    }

    public void setGenre(int genre) {
        this.genreId = genre;
    }

    public String getConsole() {
        return console;
    }

    public void setConsole(String console) {
        this.console = console;
    }
}
